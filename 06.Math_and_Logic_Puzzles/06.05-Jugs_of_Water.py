'''
06.05 Jugs of Water: You have a five-quart jug, a three-quart jug, and an unlimited supply of water (but
    no measuring cups). How would you come up with exactly four quarts of water? Note that the jugs
    are oddly shaped, such that filling up exactly "half" of the jug would be impossible.
'''

'''
Answer:
    Fill the 3-quart jug.
    Pour it into the 5-quart jug.
    Fill the 3-quart jug again.
    Pour water from the 3-quart jug into the 5-quart jug until it's full.
    Now, the 5-quart jug contains exactly 4 quarts of water.
'''